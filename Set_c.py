# Write a Python program to create an intersection, Union, difference of sets.

Odd={1,3,5,7,9,11}
Even={2,4,6,8,10,12}
print("Union: ", Odd|Even)              #  | operator returns union of sets
print("Intersection: ", Odd&Even)       #  & operator returns intersection of sets
print("Difference: " , Odd-Even)        #  - operator returns the difference of sets i.e the values present in only Odd here
print("Difference: " , Even-Odd)