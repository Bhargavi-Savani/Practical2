# Write a Python program to sum all the items in a dictionary.

sum = 0
S3_Dict = {0:10,1:20,2:30,3:40,4:50,5:60,6:70,7:80,8:90,9:100}
for i in S3_Dict:
    sum += S3_Dict[i]                           # Adds the values of all items and stores it in sum variable
print(sum)
